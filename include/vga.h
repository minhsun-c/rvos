/* PCI Configuration Space (ECAM) */
#define PCI_CONFIG_BASE 0x30000000
/* VGA Device: Bus 0, Device 1, Function 0 */
#define VGA_PCI_CONF (PCI_CONFIG_BASE + (1 << 15))

/* PCI Register Offsets */
#define PCI_REG_COMMAND 0x04
#define PCI_REG_BAR0 0x10
#define PCI_REG_BAR4 0x20

/* Assigned Physical Addresses from mtree */
#define VGA_FB_BASE 0x40000000 /* Framebuffer address */
#define VGA_IO_BASE 0x03000000 /* I/O port base address */

/* VGA Register Port Offsets (Standard VGA ports start at +0x300) */
#define VGA_REG_BASE (VGA_IO_BASE + 0x300)
#define VGA_PORT_ATTR 0xC0    /* 0x3C0 */
#define VGA_PORT_MISC 0xC2    /* 0x3C2 */
#define VGA_PORT_STATUS 0xDA  /* 0x3DA */
#define VGA_PORT_PEL_MSK 0xC6 /* 0x3C6 */
#define VGA_PORT_PEL_ADR 0xC8 /* 0x3C8 */
#define VGA_PORT_PEL_DAT 0xC9 /* 0x3C9 */

